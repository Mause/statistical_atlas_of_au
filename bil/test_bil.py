import unittest
from bil import parse_bil


class TestBIL(unittest.TestCase):
    def test_sample(self):
        header, data = parse_bil('sample')

        self.assertEqual(
            header,
            {
                'BYTEORDER': 'I',
                'NBITS': 8,
                'NROWS': 8,
                'NBANDS': 3,
                'NCOLS': 8
            }
        )

        self.assertEqual(
            data,
            [
                [
                    (0, 0, 255),
                    (0, 0, 255),
                    (0, 64, 255),
                    (0, 64, 255),
                    (0, 128, 255),
                    (0, 128, 255),
                    (0, 255, 255),
                    (0, 255, 255)
                ],
                [
                    (0, 0, 255),
                    (0, 0, 255),
                    (0, 64, 255),
                    (0, 64, 255),
                    (0, 128, 255),
                    (0, 128, 255),
                    (0, 255, 255),
                    (0, 255, 255)
                ],
                [
                    (64, 0, 128),
                    (64, 0, 128),
                    (64, 64, 128),
                    (64, 64, 128),
                    (64, 128, 128),
                    (64, 128, 128),
                    (64, 255, 128),
                    (64, 255, 128)
                ],
                [
                    (64, 0, 128),
                    (64, 0, 128),
                    (64, 64, 128),
                    (64, 64, 128),
                    (64, 128, 128),
                    (64, 128, 128),
                    (64, 255, 128),
                    (64, 255, 128)
                ],
                [
                    (128, 0, 64),
                    (128, 0, 64),
                    (128, 64, 64),
                    (128, 64, 64),
                    (128, 128, 64),
                    (128, 128, 64),
                    (128, 255, 64),
                    (128, 255, 64)
                ],
                [
                    (128, 0, 64),
                    (128, 0, 64),
                    (128, 64, 64),
                    (128, 64, 64),
                    (128, 128, 64),
                    (128, 128, 64),
                    (128, 255, 64),
                    (128, 255, 64)
                ],
                [
                    (255, 0, 0),
                    (255, 0, 0),
                    (255, 64, 0),
                    (255, 64, 0),
                    (255, 128, 0),
                    (255, 128, 0),
                    (255, 255, 0),
                    (255, 255, 0)
                ],
                [
                    (255, 0, 0),
                    (255, 0, 0),
                    (255, 64, 0),
                    (255, 64, 0),
                    (255, 128, 0),
                    (255, 128, 0),
                    (255, 255, 0),
                    (255, 255, 0)
                ]
            ]
        )


if __name__ == '__main__':
    unittest.main()
